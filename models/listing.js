const mongoose = require("mongoose");
const Schema = mongoose.Schema ;
const Review = require("./review.js");

const listingSchema = new Schema ({
    title: {
        type : String,
        required : "price requried",
        min : 0
    },
    description: String,
    image: {
        // filename: {
        //     type: String,
        //     default: "listingimage"
        // },
        // url: {
        //     type: String,
        //     default: "https://unsplash.com/photos/a-large-swan-image-url"
        // }

        url: String,
        filename: String
    },
    price: Number,
    location: String,
    country: String,
    reviews: [
    {
        type: Schema.Types.ObjectId,
        ref: "Review",
    },
    ],

    owner:{
        type: Schema.Types.ObjectId,
        ref: "User",
    },
    geometry: {
  type: {
    type: String,
    enum: ["Point"],
    required: true,
    default: "Point"
  },
  coordinates: {
    type: [Number], // [lng, lat]
    required: true
  }
}


})

listingSchema.post("findOneAndDelete", async (listing) => {
    if (listing) {
        await Review.deleteMany({
            _id: { $in: listing.reviews },
        });
    }
});


const listing = mongoose.model("listing" , listingSchema);
module.exports = listing 